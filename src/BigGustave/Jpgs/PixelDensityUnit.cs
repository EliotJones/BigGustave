namespace BigGustave.Jpgs
{
    /// <summary>
    /// The units for pixel densities.
    /// </summary>
    public enum PixelDensityUnit : byte
    {
        /// <summary>
        /// No units.
        /// </summary>
        None = 0,
        /// <summary>
        /// Pixels per inch.
        /// </summary>
        PixelsPerInch = 1,
        /// <summary>
        /// Pixels per centimeter.
        /// </summary>
        PixelsPerCm = 2
    }
}